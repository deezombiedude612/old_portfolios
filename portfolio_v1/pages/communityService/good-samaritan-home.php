					still going over to Klang.)
				</p>
				<p>
					The team and I had a few meetings together to brainstorm on what we can do to complete our 10-hour
					requirement, and it had to be done in multiple sessions and not just doing it all in one shot. As the
					only one who had actually done a community service project initiative before in the team, I was tasked
					as team lead to help share any ideas I may have given my previous experience. It eventually turns out I
					did not need to do much more than anyone else; everyone was giving their all to our activities which
					were split into 5 two-hour sessions, the first two of which were done on the same day itself.
				</p>
				<p>
					Come the days when we went over to the home, we had a few accounts of experience, fun and.. uh, not so
					good #SHTUFF of our own. Our ice-breaking activity (Activity 1) did not turn out so well as we had
					expected. Our initial idea was to get the children to visualize and draw out what they think a computer
					of the future would look like. It turns out that they did not have a slight clue as to how modern
					technology has advanced so far apart from games and the occasional big names of famous products; some
					literally had nothing come to mind and started drawing something else or fold paper airplanes! The
					original plan would have ended with us giving a small prize to those whom we think showed the most
					creativity after giving them an hour or so to carry out what we got them to do. Upon finishing the
					activity, we had ample time left and despite having not planned this at all, we proceeded with starting
					to bond with the cheeky children. Honestly, most of them were just not shy at all. They loved playing
					and interacting with us; hyperactive, always smiling, that sort of thing.. and we really loved those
					prominent aspects of their character.
				</p>
				<p>
					Our fun experiences just continued throughout Activities 2 to 4, where we got them to learn how to use
					Gmail and Microsoft Office Powerpoint, as well as a small talk on Internet Privacy. We got the green
					light for the kids to get an e-mail account from the PIC (YM Tay), and initially we told the kids that
					having an e-mail account would mean being able to contact friends for like homework or something
					important. They soon find out that they were allowed to attach pictures in the e-mails they send and
					from that point, they were sending funny GIFs to each other. The third activity was albeit uneventful,
					since all we are doing is just teaching them how to make their slides look more creative. The fourth
					activity, however, ended with pretty hilarious results. While some of us attempted to pose as scheming
					baddies trying to rip off information from the kids, how some of them responded to those messages
					within a small chat group as an alternative to social media accounts turned out to be completely
					unexpected and BONKERS. Oh well, at least they knew that they are not supposed to post any vital or
					private information online.. I think.
				</p>
				<p>
					We were told by the PIC to give them a small little test to see whether what we tried to teach them was
					fruitful or not, and we did exactly that for Activity 5.. and they did turn out to be paying attention
